ciphertext = [0x74,0x66,0x6f,0x6f,0xc3,0x47,0x6c,0x6d,0x66,0xc2,0xaf,0xc3,0x60,0x6c,0x6d,0x64,0x71,0x82,0x17,0x16,0x6f,0x82,0x17,0x6a,0x6c,0x6d,0x70,0xc2,0xc2,0xc2]

def decrypt(key):
    plaintext = ""
    for c in ciphertext:
        newchr = c - 0x2644
        newchr ^= 0x0dead
        newchr += 10
        newchr -= key
        newchr ^= key
        plaintext += chr(newchr & 0xff)
    return plaintext

for key in range(256):
    decryption = decrypt(key)
    if decryption.isprintable():
        print("Key = " + str(key))
        print("Decryption: " + decryption)
        print("=================")